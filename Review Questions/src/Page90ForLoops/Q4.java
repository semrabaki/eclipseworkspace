package Page90ForLoops;

public class Q4 {

	public static void main(String[] args) {
		//Write a program to add counting numbers from 23 to 57 by using for-loop. 
		//Print the sum on the console

		int sum = 0;
		for (int i= 23; i<58; i++)
		{
			sum = sum + i;
			
		} 
		
		System.out.println("sum of the number "+ sum);
	}

}
