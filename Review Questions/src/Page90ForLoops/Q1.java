package Page90ForLoops;

public class Q1 {

	public static void main(String[] args) {
		//1) Write a program to print counting numbers from 10 to 57 on the console by using for-loop.
		
		for (int i= 10; i<58; i++)
		{
			System.out.print(i + " ");
		} 
		
		System.out.println("===========================================");

	}

}
