package dowhileloopquestions;

public class Q4 {

	public static void main(String[] args) {
		
		/*
		 * Write a program to print letters from c to m on the console by using do-while loop.
		 */
		
		char c= 'c';
		
		do {
			
			System.out.print(c + " ");
			
			c++;
			
			
		}while(c<'n');
		
		
		
	}

}
